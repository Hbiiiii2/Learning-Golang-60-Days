// Tipe data String !

package main

import "fmt"

func main() {

	fmt.Println("Hello")
	fmt.Println("Uding Bangga")
	fmt.Println("Anak Mamah")

	fmt.Println(len("Hello"))
	fmt.Println("Uding Bangga"[2])
	fmt.Println("Anak Mamah"[3])
}

// Function : len(string) -> untuk menghitung jumlah karakter dalam string
// Function : "string"[number] -> untuk mengambil karakter pada index tertentu -> dia ambil format byte
